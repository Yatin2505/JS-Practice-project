let a=Number(prompt("Enter your percentage: "));
if(a>=90) {
    document.write(`You have got A+ grade.`);
}else if(a>=90) {
    document.write(`You have got A grade.`);
}else if(a>=75) {
    document.write(`You have got B grade.`);
}else if(a>=50) {
    document.write(`You have got C grade.`);
}else {
    document.write(`You have got D grade.`);
}